namespace ImageCharts.Net.Enums
{
    /// <summary>
    /// The visual style in which the shape marker should be displayed
    /// </summary>
    public enum  ShapeMarkerType
    {
        /// <summary>
        /// Displays a '+'
        /// </summary>
        Cross,

        /// <summary>
        /// Displays a '♦'
        /// </summary>
        Diamond,

        /// <summary>
        /// Displays a '●'
        /// </summary>
        Circle,

        /// <summary>
        /// Displays a '■'
        /// </summary>
        Square,

        /// <summary>
        /// Displays an 'X'
        /// </summary>
        X
    }
}
